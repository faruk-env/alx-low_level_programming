#include "main.h"

/**
 * print_sign - this function will print a particular sign
 *
 * @n: this parameter takes in the value we are to check
 *
 * Return: 1,0,-1
 */

int print_sign(int n)
{
	if (n > 0)
	{
		_putchar('+');
		return (1);
	}
	else if (n == 0)
	{
		_putchar('0');
		return (0);
	}
	else
	{
		_putchar('-');
		return (-1);
	}
}
